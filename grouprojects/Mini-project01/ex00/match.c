int	match(char *s1, char *s2)
{
	int i;
	int j;
	int is_match;

	i = 0;
	j = 0;
	is_match = 1;
	while (s1[i] != '\0')
	{
		while (s2[j] == '*')
		{
			j++;
			while (s1[i] != s2[j] && s2[j] != '*')
			{
				i++;
				if (s1[i] == '\0')
					is_match = 0;
			}
		}
		if (s1[i] != s2[j])
			is_match = 0;
		i++;
		if (s2[j + 1] != '\0' && s1[i] != s2[j])
			j++;
	}
	return (is_match);
}
